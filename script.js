// ==================== 导航栏滚动效果 ====================
document.addEventListener('DOMContentLoaded', function() {
    const header = document.getElementById('top-header');
    
    window.addEventListener('scroll', function() {
        if (window.scrollY > 100) {
            header.classList.add('scrolled');
        } else {
            header.classList.remove('scrolled');
        }
    });

    // ==================== 语言切换功能 ====================
    const langToggle = document.querySelector('.lang-toggle');
    const langLinks = document.querySelectorAll('.lang-menu a');
    
    // 切换语言函数
    function switchLanguage(langCode) {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[langCode] && translations[langCode][key]) {
                el.textContent = translations[langCode][key];
            }
        });
        
        // 更新页面语言属性
        document.documentElement.setAttribute('lang', langCode);
    }
    
    langLinks.forEach(link => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            const selectedLang = this.textContent;
            const langCode = this.dataset.lang;
            
            langToggle.textContent = selectedLang;
            switchLanguage(langCode);
            
            // 保存语言偏好到本地存储
            localStorage.setItem('ominimusic-lang', langCode);
        });
    });
    
    // 检查本地存储的语言偏好
    const savedLang = localStorage.getItem('ominimusic-lang');
    if (savedLang && translations[savedLang]) {
        const langLink = document.querySelector(`[data-lang="${savedLang}"]`);
        if (langLink) {
            langToggle.textContent = langLink.textContent;
            switchLanguage(savedLang);
        }
    }

    // ==================== 产品下拉菜单分类切换 ====================
    const navDropdown = document.querySelector('.nav-dropdown');
    const dropdownMenu = document.querySelector('.dropdown-menu');
    const categoryItems = document.querySelectorAll('.category-item');
    const productPanels = document.querySelectorAll('.product-panel');
    
    let closeTimeout = null;
    
    // 显示下拉菜单
    function showMenu() {
        if (closeTimeout) {
            clearTimeout(closeTimeout);
            closeTimeout = null;
        }
        dropdownMenu.classList.add('show');
    }
    
    // 隐藏下拉菜单（延迟）
    function hideMenu() {
        closeTimeout = setTimeout(() => {
            dropdownMenu.classList.remove('show');
            // 重置为默认选中状态
            categoryItems.forEach(el => el.classList.remove('active'));
            document.querySelector('.category-item[data-category="drums"]').classList.add('active');
            productPanels.forEach(panel => panel.style.display = 'none');
            document.getElementById('panel-drums').style.display = 'block';
        }, 200);
    }
    
    // 导航栏产品链接悬停
    navDropdown.addEventListener('mouseenter', showMenu);
    navDropdown.addEventListener('mouseleave', hideMenu);
    
    // 下拉菜单本身悬停（防止快速移动时消失）
    dropdownMenu.addEventListener('mouseenter', showMenu);
    dropdownMenu.addEventListener('mouseleave', hideMenu);
    
    // 分类切换
    categoryItems.forEach(item => {
        item.addEventListener('mouseenter', function() {
            categoryItems.forEach(el => el.classList.remove('active'));
            this.classList.add('active');
            
            productPanels.forEach(panel => panel.style.display = 'none');
            
            const category = this.dataset.category;
            const targetPanel = document.getElementById(`panel-${category}`);
            if (targetPanel) {
                targetPanel.style.display = 'block';
            }
        });
    });

    // ==================== 平滑滚动到锚点 ====================
    const anchorLinks = document.querySelectorAll('a[href^="#"]');
    anchorLinks.forEach(link => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            const targetId = this.getAttribute('href');
            if (targetId === '#') return;
            
            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                targetElement.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        });
    });

    // ==================== 按钮点击反馈 ====================
    const buttons = document.querySelectorAll('.btn-primary, .btn-secondary');
    buttons.forEach(button => {
        button.addEventListener('click', function(e) {
            e.preventDefault();
            this.style.transform = 'scale(0.98)';
            setTimeout(() => {
                this.style.transform = 'scale(1)';
            }, 100);
        });
    });

    // ==================== 卡片悬停动画 ====================
    const cards = document.querySelectorAll('.category-item, .app-card');
    cards.forEach(card => {
        card.addEventListener('mouseenter', function() {
            this.style.transition = 'transform 0.3s ease, box-shadow 0.3s ease';
        });
        card.addEventListener('mouseleave', function() {
            this.style.transition = 'transform 0.2s ease, box-shadow 0.2s ease';
        });
    });

    // ==================== 产品卡片加载动画 ====================
    const animateOnScroll = function() {
        const productCards = document.querySelectorAll('.product-full-width, .product-split');
        
        productCards.forEach(card => {
            const cardTop = card.getBoundingClientRect().top;
            const windowHeight = window.innerHeight;
            
            if (cardTop < windowHeight * 0.85) {
                card.style.opacity = '1';
                card.style.transform = 'translateY(0)';
            }
        });
    };

    // 初始设置
    document.querySelectorAll('.product-full-width, .product-split').forEach(card => {
        card.style.opacity = '0';
        card.style.transform = 'translateY(50px)';
        card.style.transition = 'opacity 0.8s ease, transform 0.8s ease';
    });

    // 触发动画
    window.addEventListener('scroll', animateOnScroll);
    animateOnScroll();
});